#ifndef NNUE_FEATURE_H
#define NNUE_FEATURE_H

#include <cstdint>
#include "chess.h"

namespace athena
{

    int feature_index(Square sq, PieceClass pc)
    {
        auto piece = pc.piece();
        auto color = pc.color();
        return color + (piece * 4) + (sq * 24);
    }

} // namespace athena

#endif /* NNUE_FEATURE_H */